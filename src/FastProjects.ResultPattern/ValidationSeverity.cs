namespace FastProjects.ResultPattern;

/// <summary>
/// Defines the severity levels for validation errors.
/// </summary>
public enum ValidationSeverity
{
    /// <summary>
    /// Indicates an error severity level.
    /// </summary>
    Error = 0,

    /// <summary>
    /// Indicates a warning severity level.
    /// </summary>
    Warning = 1,

    /// <summary>
    /// Indicates an informational severity level.
    /// </summary>
    Info = 2
}
